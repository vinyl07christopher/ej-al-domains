  <h4><strong>Publish Original Research</strong></h4>

  <p>When submitting your paper for publication, it should:</p>

  <ul>
    <li>Contain original research that has not been published before.</li>

    <li>Not be submitted to any other publication while you await a peer review decision.</li>
  </ul>

  <p>
    IEEE recognizes that technical research often follows an evolutionary publishing process. For example, research may be published first
    as a conference paper with preliminary findings, and then as a journal article with fully developed research and conclusions. IEEE
    supports this process provided that:
  </p>

  <ol>
    <li>The paper undergoes standard peer review every time it is submitted to a conference or publication.</li>

    <li>The later version of the article contains substantially more technical information than the earlier version.</li>

    <li>The later version cites the earlier version and clearly indicates how the two versions differ.</li>
  </ol>

