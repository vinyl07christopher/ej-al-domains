  <h4>Proper Citation Practices</h4>

  <p>
    You can improve research reproducibility with proper citation practices. Always cite your sources.&nbsp;Citation is required in several
    instances. Follow these guidelines:
  </p>

  <ul>
    <li>
      <strong>Direct quotation:&nbsp;</strong>Place verbatim text from another source in quotation marks. Indent text for longer quotes.
      Include a citation to the original source.
    </li>

    <li>
      <strong>Paraphrase or summary:&nbsp;</strong>Include a citation when restating or summarizing information from another source,
      including ideas, processes, arguments, or conclusions.
    </li>

    <li>
      <strong>Data, research results, information, graphics, or tables:&nbsp;</strong>Cite the original source when referring to, adapting,
      or reusing any information from another source.
    </li>
  </ul>

  <p>Note that the same rules apply to your own previously published work. When in doubt, include a citation.</p>

  <a name="m_-8109289627162079155_m_615488341693215190_inappropriate-citation"></a>

  <h4><strong>Inappropriate Use of Citations</strong></h4>

  <p>
    Citing an irrelevant source for the purpose of artificially inflating citation metrics is considered a breach of ethics. Only cite
    relevant sources that legitimately contribute to your paper according to the criteria outlined above.
  </p>

