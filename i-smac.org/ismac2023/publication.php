  <h4><strong>Report Data Accurately</strong></h4>

  <p>
    Readers of your paper rely on you to communicate your research findings fully and report your data accurately. Ensure you are showing
    the full picture by avoiding fabrication, falsification, and image manipulation during your research and when you are writing or
    revising your paper.&nbsp;
  </p>

  <p>Avoid:</p>

  <ul>
    <li>Fabrication: Inventing data or results.</li>

    <li>Falsification: Manipulating research materials, equipment, or processes, or changing or omitting data or results&sup1;.</li>

    <li>Image manipulation: Excessive or inappropriate adjustment of an image that alters the scientific meaning of the image.</li>
  </ul>

