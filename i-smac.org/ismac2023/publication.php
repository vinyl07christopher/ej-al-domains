  <h4>Ethical Requirements</h4>

  <p>
    Following ethical guidelines is required in scientific publishing. Understand IEEE&rsquo;s publishing guidelines and concepts to ensure
    ethical requirements are met. Learn about authorship, how to cite sources appropriately, plagiarism, how to report your data accurately,
    and the importance of publishing original research.
  </p>

  <a name="m_-8109289627162079155_m_615488341693215190_authorship"></a>

  <h4><strong>Definition of Authorship</strong></h4>

  <p>
    Who should be listed as an author on your paper? The IEEE definition of authorship will help you answer that question and clarify each
    author&rsquo;s responsibilities.
  </p>

  <p>IEEE considers individuals who meet&nbsp;<strong>all</strong>&nbsp;of the following criteria to be authors:</p>

  <ol>
    <li>
      Made a significant intellectual contribution to the theoretical development, system or experimental design, prototype development,
      and/or the analysis and interpretation of data associated with the work contained in the paper.
    </li>

    <li>Contributed to drafting the paper or reviewing and/or revising it for intellectual content.</li>

    <li>Approved the final version of the paper as accepted for publication, including references.</li>
  </ol>

  <p>
    Contributors who do not meet all of the above criteria may be included in the Acknowledgment section of the paper. Omitting an author
    who contributed to your paper or including a person who did not fulfill all of the above requirements is considered a breach of
    publishing ethics.
  </p>

