    drinking water etc. The workshops and laboratories are with equipped with state of art tools, equipments and machines for the students. There are
    100 plus teaching faculty and 60 plus administrative and supportive staff at the campus. The faculty members are well qualified and trained in
    different allied areas of engineering and technology and they have several years of experience in their respective areas of specialization.
  </p>
