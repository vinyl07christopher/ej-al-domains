  <h3>About Purwanchal Campus</h3>
  <p>
    Purwanchal Campus formerly known as Eastern Region Campus (ERC) is one of constituent campus of Tribhuvan University (TU) and one of the associate
    engineering campuses of Institute of Engineering (IOE) which is a comprehensive, non-profit making, autonomous institution and pioneering
    institution of higher education level in Nepal funded by the government of Nepal. It is situated at Gangalal Marg, Tinkune, Dharan-8, Sunsari
    district in the eastern region of Nepal. It occupies areas of 34-13-11.75 Bigahas in convenient unit, which is equivalent to about 234,870.53
    square meters. It is adjacent to Charkose Jhadi (densely forest) in the north and located at entrance gate by bus of Dharan Sub-metropolitan city.
  </p>

  <p>
    It was established in Paush, 2034 BS (1976) under the loan agreement with Asian Development Bank (ADB). In another agreement with government of
    United Kingdom (UK) held in Shrawn, 2035BS (1978), the technical assistance was provided by the government of United Kingdom (UK). ERC was
    formally organized under Institute of Engineering (IOE), Tribhuvan University (TU) and all the construction works were completed in 2043 BS
    (1984).
  </p>

  <p>
    The campus firstly started trade level courses in Mechanical, Electrical and Civil engineering streams as specified in the loan and the project
    agreements. The programs in Diploma in Mechanical, Electrical, Civil Engineering (3 years) was started since 2043 BS (1984) and also Refrigeration
    & Air-conditioning Engineering program in 2052 BS, Computer and electronics engineering program in 2059 BS. In the attempt to further up
    gradation, IOE/ Purwanchal Campus introduced Bachelor level course that was Bachelor of Engineering (BE) in Agricultural Engineering in 2057 BS
    (2000) which was first and pioneer in the country. Another BE program in Civil Engineering was commenced from 2061 BS (2004).
  </p>

  <p>
    The campus was also conducted under Council for Technical Education & Vocational Training (CTEVT) since 2045/11/12 (BS) by the mandate of
    Government of Nepal and later again came back and run under IOE/ TU since 2047/2/15 BS.
  </p>

  <p>
    Within the framework of development objectives and strategy, IOE/ Purwanchal Campus has been proposed to develop as focused in centre of
    excellence in engineering education in eastern region of the country. After successful running the programs in BE in Agricultural and Civil
    Engineering, it was started another programs in Electronics & communication Engineering, Mechanical Engineering and Computer Engineering in 2069
    (BS) and Electrical Engineering in 2071 (BS) and new program BE in Architecture in 2072 (BS). Till now there are seven BE engineering programs
    running in the campus and all trade level courses, three years diploma engineering programs was phased out as per policy of IOE/TU which was
    started since beginning of the establishment of the campus.
  </p>

  <p>
    At present there are more than 1200 students are studying in different programs. The physical facilities of IOE/ Purwanchal campus are developed
