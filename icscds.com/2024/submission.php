<p>We accept high-quality publications that describe original and previously unpublished findings from conceptual, experimental, or theoretical work or research in any of the above-mentioned topic areas. When a manuscript is submitted, it implies that it is not being considered for other publications.</p>

<p>&nbsp;</p>

<p><strong>General Guidelines</strong></p>
<ul>
<li>All manuscripts will be double-blind peer-reviewed and must fulfill the scientific standards of originality and academic excellence.</li>
<li>The Maximum page limit for a manuscript is 6-8.</li>
<li>The number of authors in a manuscript will be limited to 6. If exceeded, a detailed description should be provided on the research contribution of each author to the paper.</li>
<li>The submitting/corresponding author is responsible for ensuring that all other co-authors have authorized the article's publication and assumes responsibility for the manuscript throughout submission and peer review.</li>
<li>The corresponding author will receive an email notification regarding the status of the submitted manuscript after review. Only the authors of the accepted paper can register to the conference.</li>
<li>Should any compensation claims be made, the publisher will not be held legally liable</li>
</ul>
<p>&nbsp;</p>
<p><strong>Language</strong></p>
<p>The article should be written in English; either American or British use is acceptable, but both should not be mixed.</p>
<p>Authors should proofread their English language manuscript before submission to eliminate possible grammatical or spelling errors and to conform to correct scientific English. Poor English can also result in the rejection of the manuscript.</p>
<p>&nbsp;</p>

<p><strong>Submission Method</strong></p>
<p style='background:#fff;color:#000;padding:.5rem;'>Please send your full paper via Email to <a style='color:blue;' href="mailto:icscds.conference@gmail.com">icscds.conference@gmail.com</a>. Once the paper is received, author will receive a submission acknowledgement mail as a reply.</p>
<p>For any queries about submission, please contact us at <a  href="mailto:icscds.conference@gmail.com">icscds.conference@gmail.com</a>.</p>
<p>&nbsp;</p>
<p><strong>Academic &amp; Publication Ethics</strong></p>
<p>Articles submitted to the conference should provide new and previously unpublished research. In the academic context, adhering to ethical standards is important. As a result, any act of plagiarism will not be allowed. If an author is discovered to have plagiarised, the submission will be automatically rejected.</p>
<p><a  href="https://www.ieee.org/publications_standards/publications/rights/plagiarism.html">IEEE Web Page on Plagiarism</a></p>
<p><a  href="https://www.ieee.org/publications_standards/publications/rights/Multi_Sub_Guidelines_Intro.html">IEEE Web Page on Double Submission</a></p>

<p><strong>Evaluation Process</strong></p>
<p>All articles will go through double-blind peer review. Relevancy, structure, research technique, ethical conducts, language standard, innovativeness, references, hypothesis, result presentation, proficiency, format, and a variety of other aspects are all taken into account throughout the evaluation process.</p>
<p>Papers that are accepted and registered will be published in the Conference Proceedings.</p>
<p>&nbsp;</p>
<p><strong>Instructions for the Oral Presentation</strong></p>
<p>The accepted and registered papers will only be allotted with the presentation slot. The length of the presentation should be 20 minutes. The authors  can use any form of presentation. Kindly arrive early  for the presentation to ensure necessary arrangements.

