<p><em>While submitting the research article, authors should ensure that the research work is novel and has not previously been published, as well as that it is not currently being considered for publication in any other conferences/journals.</em></p>
<p>The following are the instructions and guidelines to submit your manuscript to ICEAI 2023.</p>
<p>&nbsp;</p>
<p><strong>Formatting and Paper Template</strong></p>
<ul>
<li><p>
The manuscript file should start with a title that shows author affiliations and contact information, identifying the corresponding author with a special symbol.
</p>
The following structure will be suitable in many cases:
<ul class='mb-3'>
<li>Introduction</li>
<li>Literature Review</li>
<li>Methods</li>
<li>Results &amp; Discussion</li>
<li>References</li>
<li>Acknowledgements (optional)</li>
<li>Author contributions (if paper has more than 6 authors)</li>
</ul>
</li>
<li>There can be a maximum of 4 authors in a paper. If exceeds, the individual contribution made by each author should be mentioned. Do not use academic titles such as Dr., Professor, HoD, Director, etc. in your final paper.</li>
<li>Follow the single-column format and the content should fit in the A4 Size</li>
<li>The size of the paper is limited to 12-15 pages.</li>
<li>Authors are requested to submit the paper by clearly stating the goals, methods and research findings of the chosen topic.</li>
<li>The paper should be comprehensive, informative, and appealing to the readers.</li>
<li>Figures should be incorporated with the manuscript text into a single Word doc or PDF at initial submission. Please include a high-resolution image.</li>
<li>Write paper in English language [Either in American/British but not both], which will be readily understood by any researcher.</li>
</ul>
<p>&nbsp;</p>
