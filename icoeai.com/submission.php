<p><strong>Review Process</strong></p>
<p>ICEAI 2023 will follow a peer-review process. All submitted papers will be reviewed based on quality, presentation and relevance to the conference theme. Papers that do not conform to the submission policies will be rejected without review. The most frequent cause of rejections of submitted papers is a lack of new results and plagiarized content. Duplicated submissions to concurrent conferences must be avoided.</p>
<ul>
<li>The conference chair examines all peer reviewer input before making an informed choice to accept or reject the submission.</li>
<li>This process takes 2-3 weeks after initial submission.</li>
<li>The decision of acceptance/rejection/revision will be communicated via Email to the corresponding author.</li>
</ul>
<p><em>Note: Authors should register only when their work has been accepted.</em></p>
<p>&nbsp;</p>
<p><strong>Terms and Conditions:</strong></p>
<ul>
<li>Duplicated submissions to concurrent conferences must be avoided.</li>
<li>The Corresponding Author should provide his/her contact details to ICEAI for the purpose of the conference. It is the sole responsibility of the Corresponding Author to communicate with co-authors.</li>
<li>Presenters may be recorded for live streaming and a recording of the presentation and a copy of the slides may be published online after the conference.</li>
<li>The submission of a paper constitutes a formal commitment by the author[s] to present the work (if accepted) in the form, session and time assigned by the Programme Committee.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Publication Ethics and Conference Policy:</strong></p>
<p>Papers are assessed on the basis that they are not plagiarized&nbsp;and have not been submitted to another conference at the same time (double submission). These are significant issues, and ICDICI&nbsp;will take appropriate action against any author who engages in either activity.</p>
<p>If we discover that you have plagiarised other people's work or used third-party copyright material without permission or with inadequate acknowledgement, or if the authorship of the piece is disputed, we retain the right to take action, including retracting the paper from publication.</p>
<p>The author must notify the conference chair of any prior distribution and/or publishing of any portion of the content, even if the article has been shared as a preprint.</p>
<p>Authors are responsible for seeking permission from copyright holders before duplicating any previously published graphics, tables, figures, or extensive quotes.</p>
<p>ICEAI reserves the right to retract a paper from proceedings after the conference if the paper is submitted in any other forum.</p>
